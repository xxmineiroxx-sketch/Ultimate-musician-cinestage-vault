// Clears all existing songs and imports the full TBLC library fresh.
// This guarantees every song gets its complete data (YouTube link, keys, BPM, theme).
export async function ensureTblcSeeded() {
  try {
    await clearSongs();
    let count = 0;
    for (const s of TBLC_SONGS) {
      await addOrUpdateSong({ id: makeId("song"), timeSig: "4/4", ...s });
      count++;
    }
    return { status: "seeded", count };
  } catch (e) {
    return { status: "error", error: String(e) };
  }
}
